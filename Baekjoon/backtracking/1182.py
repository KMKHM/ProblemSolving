n, s = map(int,input().split())
n_list = list(map(int,input().split()))

cnt = 0
res = []

visited = [0] * n

def dfs(num, ls):
    global cnt

    if num == n:
        return

    ls += n_list[num]

    res.append([num, ls])

    if ls == s:
        print(num)
        print("-----")
        cnt += 1

    dfs(num + 1, ls)
    dfs(num + 1, ls-n_list[num])

dfs(0, 0)

print(cnt)

