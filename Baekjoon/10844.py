"""
쉬운 계단 수
문제: https://www.acmicpc.net/problem/10844
"""
n = int(input())
# 1 2 3 4 5 6 7 8 9 => 9
# 10 12 21 23 32 34 43 45 54 56 65 67 76 78 87 89 98 => 1 + 8*2=>17
# 끝자리가 0이나 9이면 경우의 수 1개 나머지는 2개씩 => 2 + 15 * 2 => 32
# 3 + 29 * 2
d = 1_000_000_000
dp = [0] * (n + 1)
dp[1] = 9

tmp = 1

for i in range(2, n+1):
    dp[i] = (tmp + (dp[i-1] - tmp) * 2)
    tmp += 1

print(dp[n]%d)